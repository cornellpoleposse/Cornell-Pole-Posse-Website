    <h3>What to wear</h3>
    <p>Feel free to wear anything you are comfortable in! However, it is better to wear less clothes, as skin to pole contact is very important. It is recommended to wear something like shorts, spandex, a sports bra, etc. </p>

    <h3>Join Us!</h3>
    <p>Looking to join Cornell Pole Posse? Just show up at any of our practices! Anyone and everyone is welcome regardless of experience with pole dancing. You must sign a waver before starting to practice.</p>

